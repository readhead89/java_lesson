public class For {
    public static void main(String[] args) {
        for (int i = 10; i >=0; i--) {
            System.out.println("Hello " + i);
        }
        int x = 1;
        if (x<10) {
            System.out.println("New");
        }
        else {
            System.out.println("old");
        }
    }
}
